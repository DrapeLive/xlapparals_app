class Agent {
  final int id;
  final String role;
  final int userId;
  final String username;
  final String email;
  final String contact;
  final int totalCustomers;

  Agent({
    required this.id,
    required this.role,
    required this.userId,
    required this.username,
    required this.email,
    required this.contact,
    required this.totalCustomers,
  });
}
